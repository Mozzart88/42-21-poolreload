int		ft_sqrt(int nb)
{
	int a;
	int i;

	a = 0;
	i = 1;
	if (nb > 0 && nb <= 2147395600)
	{
		while (a * a <= nb)
		{
			if (a * a == nb)
				return (a);
			a++;
		}
	}
	return (0);
}
